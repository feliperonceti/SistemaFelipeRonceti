    public PedidoFgv viewBean() {
        PedidoFgv pedidoFgv = new PedidoFgv();
        int id = Integer.valueOf(jTxtCodigo.getText());
        pedidoFgv.setIdpedidoFgv(id);
        pedidoFgv.setNomeclienteFgv(jTxtNome.getText());
        pedidoFgv.setEnderecoentregaFgv(jTxtEnderecoentrega.getText());
        pedidoFgv.setModelotelefoneFgv(jTxtModelotelefone.getText());
        pedidoFgv.setDataentregaFgv(Util.strDate(jFmtDataentrega.getText()));
        pedidoFgv.setValorpedidoFgv(jTxtValorpedido.getText());
        return pedidoFgv;

       
    }
      
       
        public void beanView(PedidoFgv pedidoFgv) {
        String cad = String.valueOf(pedidoFgv.getIdpedidoFgv());
        jTxtCodigo.setText(cad);        
        jTxtNome.setText(pedidoFgv.getNomeclienteFgv());
        jTxtEnderecoentrega.setText(pedidoFgv.getEnderecoentregaFgv());
        jTxtModelotelefone.setText(pedidoFgv.getModelotelefoneFgv());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtDataentrega.setText( formato.format(pedidoFgv.getDataentregaFgv()));
        jTxtValorpedido.setText(pedidoFgv.getValorpedidoFgv());
        

        }
